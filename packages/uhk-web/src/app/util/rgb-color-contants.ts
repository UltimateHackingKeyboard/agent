import { RgbColorInterface } from 'uhk-common';

export function whiteRgbColor(): RgbColorInterface  {
    return {
        r: 255,
        g: 255,
        b: 255,
    };
}

export function blackRgbColor(): RgbColorInterface  {
    return {
        r: 0,
        g: 0,
        b: 0,
    };
}

export function keyboardGreyRgbColor(): RgbColorInterface  {
    return {
        r: 51,
        g: 51,
        b: 51,
    };
}
